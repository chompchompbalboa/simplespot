//-----------------------------------------------------------------------------
// Requires
//-----------------------------------------------------------------------------

var isset = require('./isset');

//-----------------------------------------------------------------------------
// Module
//-----------------------------------------------------------------------------

var settings = {

    //-----------------------------------------------------------------------------
    // Fetch
    //-----------------------------------------------------------------------------

    fetch: function(auto, settings) {
        var fetch = {};
        for (let current in auto) {
            if(isset.is(settings[current])) {
                fetch[current] = settings[current];
            }
            else {
                fetch[current] = auto[current];
            }
        }
        return fetch;
    }
};

//-----------------------------------------------------------------------------
// Export
//-----------------------------------------------------------------------------
module.exports = settings;