//-----------------------------------------------------------------------------
// BasicModule
//-----------------------------------------------------------------------------
// The basic module I use as a placeholder while building new features
//
//-----------------------------------------------------------------------------
// Requires
//-----------------------------------------------------------------------------
var React = require('react');
var Radium = require('radium');
//-----------------------------------------------------------------------------
// Module
//-----------------------------------------------------------------------------
var BasicModule = React.createClass({

    //-------------------------------------------------------------------------
    // Style
    //-------------------------------------------------------------------------

    style: function(settings) {
        var style = {
            section: settings.style.section
        };

        return style;
    },

    //-------------------------------------------------------------------------
    // Render
    //-------------------------------------------------------------------------

    render: function() {
        var {module, ...other} = this.props;
        var style = this.style(module.settings);

        return (
            <section className="basic-module" style={style.section}> 
                {module.settings.data.text}
            </section>
        )
    }
    
});
    
//-----------------------------------------------------------------------------
// Export
//-----------------------------------------------------------------------------
module.exports = Radium(BasicModule);